#!/usr/bin/env python
# coding: utf8
from gluon import *

class Apuesta:

    def __init__(self, cedula, nombre, monto, fecha):
        self.__cedulaApostador = cedula
        self.__nombreApostador = nombre
        self.__montoApuesta = monto
        self.__fechaApuesta = fecha
        
    def setCedula(self, cedula):
        self.__cedulaApostador = cedula
        
    def getCedula(self):
        return self.__cedulaApostador
        
    def setNombre(self, nombre):
        self.__nombreApostador = nombre
        
    def getNombre(self):
        return self.__nombreApostador
        
    def setMontoApuesta(self, monto):
        self.__montoApuesta = monto
        
    def getMontoApuesta(self):
        return self.__montoApuesta
        
    def setFechaApuesta(self, fecha):
        self.__fechaApuesta = fecha
        
    def getFechaApuesta(self):
        return self.__fechaApuesta
